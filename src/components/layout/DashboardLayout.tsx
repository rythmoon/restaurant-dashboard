// DashboardLayout.tsx
import React, { useState } from 'react';
import { Bell, Settings, User, ChefHat, Sparkles, Menu, X, LogOut } from 'lucide-react';
import { useAuth } from '../../hooks/useAuth';

interface DashboardLayoutProps {
  children: React.ReactNode;
}

const DashboardLayout: React.FC<DashboardLayoutProps> = ({ children }) => {
  const [mobileMenuOpen, setMobileMenuOpen] = useState(false);
  const { user, signOut } = useAuth();

  const handleLogout = async () => {
    if (window.confirm('¿Estás seguro de que quieres cerrar sesión?')) {
      await signOut();
    }
  };

  return (
    <div className="min-h-screen bg-gray-50">
      {/* Header moderno */}
      <header className="bg-white border-b border-gray-200">
        <div className="max-w-7xl mx-auto px-4 sm:px-6 py-4">
          <div className="flex items-center justify-between">
            {/* Logo y título */}
            <div className="flex items-center space-x-3">
              {/* Botón menú móvil */}
              <button
                onClick={() => setMobileMenuOpen(!mobileMenuOpen)}
                className="lg:hidden p-2 text-gray-600 hover:text-gray-900"
              >
                {mobileMenuOpen ? <X size={20} /> : <Menu size={20} />}
              </button>

              <div className="flex items-center space-x-3">
                <div className="relative">
                  <div className="w-10 h-10 sm:w-12 sm:h-12 bg-white rounded-2xl flex items-center justify-center shadow-sm border border-gray-200">
                    <ChefHat className="h-5 w-5 sm:h-6 sm:w-6 text-gray-700" />
                  </div>
                </div>
                <div>
                  <h1 className="text-xl sm:text-2xl font-bold text-gray-900">
                    Mary's Restaurant
                  </h1>
                  <p className="text-gray-600 text-xs sm:text-sm font-medium hidden sm:block">
                    {user ? `Conectado como: ${user.name}` : 'Dashboard Restaurant'}
                  </p>
                </div>
              </div>
            </div>

            {/* Navegación desktop - CON BOTÓN DE LOGOUT */}
            <div className="hidden lg:flex items-center space-x-4">
              {/* Información del usuario */}
              <div className="flex items-center space-x-3 p-2 rounded-xl bg-gray-50 border border-gray-200">
                <div className="w-8 h-8 bg-gray-600 rounded-full flex items-center justify-center">
                  <User className="h-4 w-4 text-white" />
                </div>
                <div className="text-left">
                  <div className="text-sm font-semibold text-gray-900">{user?.name}</div>
                  <div className="text-xs text-gray-600 capitalize">{user?.role}</div>
                </div>
              </div>

              {/* Iconos de acción */}
              <div className="flex items-center space-x-2">
                <button className="relative p-2 text-gray-600 hover:text-gray-900 transition-colors duration-200">
                  <Bell size={20} />
                  <span className="absolute -top-1 -right-1 w-2 h-2 sm:w-3 sm:h-3 bg-red-500 rounded-full border-2 border-white"></span>
                </button>
                <button className="p-2 text-gray-600 hover:text-gray-900 transition-colors duration-200">
                  <Settings size={20} />
                </button>
                <button
                  onClick={handleLogout}
                  className="p-2 text-gray-600 hover:text-gray-900 hover:bg-gray-100 rounded-lg transition-colors duration-200"
                  title="Cerrar Sesión"
                >
                  <LogOut size={20} />
                </button>
              </div>
            </div>

            {/* Perfil móvil */}
            <div className="lg:hidden flex items-center space-x-2">
              <button className="relative p-2 text-gray-600 hover:text-gray-900">
                <Bell size={18} />
              </button>
              <div className="flex items-center space-x-2">
                <div className="text-right">
                  <div className="text-xs font-semibold text-gray-900">{user?.name}</div>
                  <div className="text-xs text-gray-600 capitalize">{user?.role}</div>
                </div>
                <button
                  onClick={handleLogout}
                  className="p-2 text-gray-600 hover:text-gray-900 rounded-lg"
                >
                  <LogOut size={18} />
                </button>
              </div>
            </div>
          </div>
        </div>

        {/* Menú móvil - CON OPCIÓN DE LOGOUT */}
        {mobileMenuOpen && (
          <div className="lg:hidden bg-white border-t border-gray-200">
            <div className="px-4 py-3 space-y-3">
              <div className="flex items-center space-x-3 p-3 bg-gray-50 rounded-xl border border-gray-200">
                <div className="w-10 h-10 bg-gray-600 rounded-full flex items-center justify-center">
                  <User className="h-5 w-5 text-white" />
                </div>
                <div className="text-left">
                  <div className="text-sm font-semibold text-gray-900">{user?.name}</div>
                  <div className="text-xs text-gray-600 capitalize">{user?.role}</div>
                  <div className="text-xs text-gray-500">Usuario: {user?.username}</div>
                </div>
              </div>
              
              <div className="flex space-x-2">
                <button className="flex-1 text-center p-3 bg-gray-100 rounded-lg text-sm font-medium text-gray-700">
                  Configuración
                </button>
                <button
                  onClick={handleLogout}
                  className="flex-1 text-center p-3 bg-gray-100 rounded-lg text-sm font-medium text-gray-700"
                >
                  Cerrar Sesión
                </button>
              </div>
            </div>
          </div>
        )}
      </header>

      {/* Main Content */}
      <main className="max-w-7xl mx-auto px-4 sm:px-6 lg:px-8 py-6">
        {children}
      </main>

      {/* Footer sutil */}
      <footer className="bg-white border-t border-gray-200 mt-auto">
        <div className="max-w-7xl mx-auto px-4 sm:px-6 py-4 text-center">
          <p className="text-gray-600 text-sm">
            © 2025 MARY'S RESTAURANT.
            {user && ` | Conectado como: ${user.name}`}
          </p>
        </div>
      </footer>
    </div>
  );
};

export default DashboardLayout;
